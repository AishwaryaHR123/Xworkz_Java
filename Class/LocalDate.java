package com.xworkz.Class;

public class LocalDate {
	private int date;
	private String institue;

	public LocalDate() {
	}

	public LocalDate(int date, String institue) {
		super();
		this.date = date;
		this.institue = institue;
	}

	public LocalDate(int date) {
		super();
		this.date = date;
	}

}
